const formatarTelefone = (telefone) => {
  // Lógica para formatar o telefone (exemplo)
  return telefone.replace(/\D/g, ''); // Remove todos os caracteres não numéricos
};


module.exports = formatarTelefone;  // Exporte a função corretamente
